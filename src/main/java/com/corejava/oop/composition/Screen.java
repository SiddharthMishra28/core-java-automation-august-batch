package com.corejava.oop.composition;

public class Screen {
    private String type;
    private double size;
    private long colors;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getSize() {
        return size;
    }

    public void setSize(double size) {
        this.size = size;
    }

    public long getColors() {
        return colors;
    }

    public void setColors(long colors) {
        this.colors = colors;
    }
}
